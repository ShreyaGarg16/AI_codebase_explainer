import requests

OLLAMA_URL = "http://localhost:11434/api/generate"
MODEL = "phi3"

def call_llm(prompt: str) -> str:
    response = requests.post(
        OLLAMA_URL,
        json={
            "model": MODEL,
            "prompt": prompt,
            "stream": False,
            "options":{
                "temperature":0.2,
                "num_predict":300
            }
        },
        timeout=300
    )

    response.raise_for_status()
    return response.json()["response"]
